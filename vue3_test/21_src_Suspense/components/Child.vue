<template>
    <div class="Child">
      <h3>我是Child组件(子)</h3>
      {{sum}}
    </div>
  </template>
  
  <script>
   import { ref } from 'vue';




  export default {
    name:'Child',
    async setup(){
      let sum = ref(0)
     let p= new Promise((resolve,reject)=>{
        setTimeout(()=>{
          resolve({sum})
        },1000)
       console.log(reject);
      })
      return await p
    }
  }
  </script>
  
